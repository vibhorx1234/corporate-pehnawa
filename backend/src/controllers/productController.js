const Product = require('../models/Product');

// Get all products
exports.getAllProducts = async (req, res) => {
  try {
    const { featured, collection, inStock } = req.query;
    let query = {};
    
    if (featured === 'true') query.featured = true;
    if (collection) query.collection = collection;
    if (inStock !== undefined) query.inStock = inStock === 'true';
    
    const products = await Product.find(query)
      .populate('collection', 'name slug')
      .sort({ order: 1, createdAt: -1 });
    
    res.status(200).json({
      success: true,
      count: products.length,
      data: products
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: 'Error fetching products',
      error: error.message
    });
  }
};

// Get products by collection slug
exports.getProductsByCollection = async (req, res) => {
  try {
    const Collection = require('../models/Collection');
    const collection = await Collection.findOne({ slug: req.params.collectionSlug });
    
    if (!collection) {
      return res.status(404).json({
        success: false,
        message: 'Collection not found'
      });
    }
    
    const products = await Product.find({ collection: collection._id })
      .populate('collection', 'name slug')
      .sort({ order: 1, createdAt: -1 });
    
    res.status(200).json({
      success: true,
      count: products.length,
      collection: collection,
      data: products
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: 'Error fetching products',
      error: error.message
    });
  }
};

